/*

Data types specifies the different sizes and values that can be stored in variable

Two types of Data types,

Primitive Datatypes

    Boolean

        boolean -   1 bit, 
                    true or false, 
                    declare -> Boolean a = 0;
    
    Numeric
    
        Character
            char -  2 bytes, 
                    \u0000 to \uffff, 
                    declare -> char a = '\u0000';(Unicode value)
        
        Integer

            Integer
    
                byte -  1 byte, 
                        -128 to 127, 
                        declare -> byte a = 0;
        
                short - 2 bytes, 
                        -32768 to 32767, 
                        declare -> short a = 0;
        
                int -   4 bytes, 
                        - 2,147,483,648 (2^31) to 2,147,483,647 (2^31-1), 
                        declare -> int a = 0;
        
                long -  8 bytes, 
                        -9,223,372,036,854,775,808 (2^63) to 9,223,372,036,854,775,807 (2^63-1), 
                        declare -> long a =  0L;
        
            Floating Point
        
                float - 4 bytes, 
                        IEEE 754(32 bit) unlimited range, 
                        declare -> float a = 0.0f;
            
                double -    8 bytes, 
                            IEEE 754(64 bit) unlimited range, 
                            declare -> double a = 0.0d;

Non - Primitive Datatypes

    String,
    Array,
    Class,
    etc...

*/