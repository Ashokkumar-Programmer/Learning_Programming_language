/*

Do-While loop:

It is used for iterative process untill the boolean condition becomes false.

When the no. of condition is not fixed then use do-while or while.

While loop is used when check the condition first then execute the block of code.

Do-While loop is used when first execute the block of code then check the condition.

Syntax:

do{
	block of code
}while(boolean_condition);

______________________________________________________________________________________________________

Infinite do-while loop:

do{
	block of code
}while(true);

______________________________________________________________________________________________________

*/